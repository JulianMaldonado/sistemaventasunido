    private void btGuardaMarcaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btGuardaMarcaActionPerformed
        // TODO add your handling code here:
        
 
          String cod=txtCodigoMarca.getText();
          String des=txtDescripcionMarca.getText();
                          
        try {
           
            if(  !des.equals("") )   
                //enviar datos a validar
               
	                        {
	                         
                                       cts=cn.prepareCall("{ call GuardaMarca(?,?)}");
            
            cts.setString(1, cod);
            cts.setString(2, des);
           
            
            int rpta=cts.executeUpdate();
            if(rpta==1)
                
                JOptionPane.showMessageDialog(this,"CATEGORIA REGISTRADO CORRECTAMENTE!!","Atencion",JOptionPane.INFORMATION_MESSAGE);
           
            
                 this.dispose();
                                    
                                 
	                        }
            
            else
            
                            {
	                        JOptionPane.showMessageDialog(null, "Debe llenar el campo DESCRIPCION .\n" +
	                            "no puede quedar vacio!...");
                                
                                txtDescripcionMarca.requestFocusInWindow();
                                
                                
	                    }
        
            
           
        } catch (SQLException | HeadlessException e) {
            Logger.getLogger(Clientes.class.getName()).log(Level.SEVERE, null, e);
        }
                  

        
        
        
    }//GEN-LAST:event_btGuardaMarcaActionPerformed

